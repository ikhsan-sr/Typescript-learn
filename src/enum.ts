// Enum
// Biasa digunakan untuk menyimpan data konstan dalam level aplikasi

// numeric enums
enum Month {
  JAN = 1,
  FEB,
  MAR,
  APR,
  MAY,
}

console.log(Month.MAR);

// String Enums
enum MonthString {
  JAN = "Januari",
  FEB = "Februari",
  MAR = "Maret",
  APR = "April",
  MAY = "Mei",
}

console.log(MonthString.FEB);
